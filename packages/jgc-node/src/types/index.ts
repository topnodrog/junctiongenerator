export interface OutPoint {
  txid: Hash256;
  vout: number;
}

export interface TxInput {
  prevOut: OutPoint;
  scriptSig: string;   // hex-encoded
  sequence: number;    // 0xFFFFFFFF default
  witness?: string[];  // SegWit-equivalent (JGC supports native witness)
}

export interface TxOutput {
  value: JGCSatoshis;
  scriptPubKey: string;  // hex-encoded P2PKH or P2WPKH
}

export interface Transaction {
  version: number;
  inputs: TxInput[];
  outputs: TxOutput[];
  locktime: number;
  /** JGC-specific: if this tx carries a broker payment, reference the task. */
  brokerTaskRef?: string;
}

// ─────────────────────────────────────────────────────────────────────────────
// Epoch State (24-hour payout accumulator)
// ─────────────────────────────────────────────────────────────────────────────

/**
 * EpochState — tracks the rolling 144-block (24h) contribution window.
 *
 * Unlike Bitcoin where each block independently pays a coinbase reward to one
 * miner, JGC accumulates all TFLOPS contributions across 144 blocks and
 * settles proportional payouts at the epoch boundary (every 144th block).
 *
 * Settlement formula:
 *   minerPayout(m) = epochRewardPool × (minerTFLOPS(m) / totalEpochTFLOPS)
 *
 * where epochRewardPool = blockReward(currentEra) × 144
 */
export interface EpochState {
  /** Block height at which this epoch started (always a multiple of 144). */
  epochStartHeight: BlockHeight;

  /** Current block index within the epoch [0, 143]. */
  epochBlockIndex: number;

  /** Total accumulated TFLOPS-seconds verified in this epoch so far. */
  totalEpochTFLOPS: number;

  /** Per-miner TFLOPS accumulator: address → TFLOPS-seconds. */
  minerContributions: Map<Address, number>;

  /** Accumulated JGC reward pool for this epoch (in satoshis).
   *  Incremented by blockReward each block; paid out at epoch boundary. */
  pendingRewardPool: JGCSatoshis;

  /** UNIX timestamp of epoch start block. */
  epochStartTime: UnixTimestamp;
}

// ─────────────────────────────────────────────────────────────────────────────
// Emission Schedule Types
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Describes one "era" in the JGC emission schedule.
 *
 * BITCOIN COMPARISON:
 *   Bitcoin: reward(n) = floor(50e8 / 2^n) satoshis, n = floor(height/210000)
 *   JGC:     reward(n) = floor(50e8 × 0.75^n) satoshis, n = floor(height/105000)
 *
 *   Era 0:  50.000000000 JGC × 105,000 = 5,250,000 JGC
 *   Era 1:  37.500000000 JGC × 105,000 = 3,937,500 JGC
 *   Era 2:  28.125000000 JGC × 105,000 = 2,953,125 JGC
 *   ...
 *   Sum → 5,250,000 / (1 - 0.75) = 21,000,000 JGC  (geometric series)
 */
export interface EmissionEra {
  eraIndex: number;
  startHeight: BlockHeight;
  endHeight: BlockHeight;       // startHeight + 105,000 - 1
  blockRewardSatoshis: JGCSatoshis;
  dailyEmissionJGC: number;     // informational: blockReward × 144 / 1e16
  totalEraSupply: JGCSatoshis;  // blockReward × 105,000
}

// ─────────────────────────────────────────────────────────────────────────────
// Difficulty Adjustment
// ─────────────────────────────────────────────────────────────────────────────

/**
 * DifficultyTarget — PoUC equivalent of Bitcoin's nBits.
 *
 * BITCOIN: nBits encodes a 256-bit hash target T such that SHA256d(header) < T.
 *
 * JGC: difficultyBits encodes a minimum TFLOPS-seconds threshold T such that
 *      sum(tflopsWeight for all proofs in block) >= T.
 *
 * Retargeting window: every 2016 blocks (same as Bitcoin's 2-week window).
 * Formula: newTarget = oldTarget × (actualTimespan / expectedTimespan)
 *          where expectedTimespan = 2016 × 600 seconds = 1,209,600 s
 *          Clamped to 1/4 × old ≤ new ≤ 4 × old (same clamp as Bitcoin).
 */
export interface DifficultyTarget {
  /** Compact bits encoding (same format as Bitcoin's nBits). */
  compactBits: number;

  /** Expanded TFLOPS-seconds minimum for a valid block. */
  minTFLOPS: number;

  /** Block height at which this target became active. */
  activationHeight: BlockHeight;
}

// ─────────────────────────────────────────────────────────────────────────────
// Compute Broker Types
// ─────────────────────────────────────────────────────────────────────────────

/**
 * A bid from a commercial entity or scientific platform wanting compute time.
 * The broker ranks bids by price per TFLOPS-hour and routes idle nodes.
 */
export interface ComputeBid {
  bidId: string;
  bidderAddress: Address;
  taskType: ComputeTaskType;
  /** Maximum price bidder will pay, in JGC satoshis per TFLOPS-hour. */
  pricePerTFLOPSHour: JGCSatoshis;
  /** Minimum TFLOPS capacity required (screen out underpowered nodes). */
  minTFLOPSRequired: number;
  /** Total TFLOPS-hours requested. */
  totalTFLOPSHours: number;
  /** Task payload hash — content-addressed task bundle (model weights, dataset). */
  taskPayloadHash: Hash256;
  /** Bid expiry as UNIX timestamp. */
  expiresAt: UnixTimestamp;
  /** Whether this is a scientific fallback (Folding@Home etc.) — lower priority. */
  isFallback: boolean;
}

/**
 * Assignment record: a specific node assigned to a specific bid's work unit.
 */
export interface ComputeAssignment {
  assignmentId: string;
  bidId: string;
  nodePublicKey: PublicKey;
  assignedTFLOPS: number;
  startedAt: UnixTimestamp;
  deadlineAt: UnixTimestamp;
  /** ZK proof submitted upon task completion — verified before payment release. */
  completionProof?: ComputeProof;
  status: "PENDING" | "IN_PROGRESS" | "COMPLETED" | "FAILED" | "EXPIRED";
}

